/**
 * Type for all available link keys
 */
export type LinkKey = keyof typeof links;

/**
 * Utility function to get a link by key path
 * Usage: getLink('study.inbdeBootcamp') or getLink('states.florida.dentalHygieneApp')
 */
export function getLink(keyPath: string): string | undefined {
  const keys = keyPath.split(".");
  let current: unknown = links;

  for (const key of keys) {
    if (current && typeof current === "object" && key in current) {
      current = (current as Record<string, unknown>)[key];
    } else {
      return undefined;
    }
  }

  return typeof current === "string" ? current : undefined;
}

/**
 * Utility function to validate if a link exists
 */
export function hasLink(keyPath: string): boolean {
  return getLink(keyPath) !== undefined;
}

/**
 * Utility function to get all links in a category
 */
export function getLinksInCategory(category: LinkKey): Record<string, unknown> {
  return links[category] || {};
}

/**
 * Utility function to search for links by partial key match
 */
export function searchLinks(query: string): Array<{ key: string; value: string }> {
  const results: Array<{ key: string; value: string }> = [];

  function searchObject(obj: Record<string, unknown>, prefix = ""): void {
    for (const [key, value] of Object.entries(obj)) {
      const fullKey = prefix ? `${prefix}.${key}` : key;

      if (typeof value === "string") {
        if (
          key.toLowerCase().includes(query.toLowerCase()) ||
          value.toLowerCase().includes(query.toLowerCase())
        ) {
          results.push({ key: fullKey, value });
        }
      } else if (typeof value === "object" && value !== null) {
        searchObject(value as Record<string, unknown>, fullKey);
      }
    }
  }

  searchObject(links);
  return results;
}
